</span>So it began...<br><br>
  <p><strong>Oppiliappan Koil SrI Varadachari Sadagopan Swami: </strong>The eBooks are the final stage of the likhita kaimkaryam begun by Sri V Sadagopan Swami in June 1995. Hailing from the glorious Divya Desam of Thiruvinnagaram, and belonging to an illustrious family of Vaidika scholars and followers of SrI Ahobila Matam, SrI V.Sadagopan Swami graduated from the prestigious Massachusetts&rsquo; Institute of Technology, USA., with a Doctor Of Science Degree in Material Science and then worked for IBM Corp., for 26 Years in addition to an year at the UN in Geneva and New York in the staff of the Secretary General. Post retirement from IBM, he devoted his energies to serve the broader SrI Vaishnava community.</p>
  <p>By his own self-deprecating and amused acknowledgement, SrI V sadagopan Swami says that he has always been writing about one thing or another! The many pages of neatly handwritten manuscripts that he has created on Divya Desams, Works of Swami Desikan and other Purvacharyas, are eloquent testimonials for his prolific writing and the complete blessing of Sri Hayagriva kataksham on him. &nbsp;It is a mark of his genius that in spite of the common thread of philosophy that discusses about the greatness of Bhagavan, every book he has written or article he has posted is unique and can be savored as a special experience. His presence on the Internet &nbsp;on &nbsp;things related to sampradayam and Sri Suktis began in 1995. &nbsp;Please click the link below to read the first Introduction eMail posted by Sri Sadagopan Swami in June 1995 to the erstwhile bhakti list group that was the precursor to the many yahoo groups that abounded in the later years.&nbsp;&nbsp;<a href="http://www.ramanuja.org/sv/bhakti/archives/jun95/0023.html" target="_blank" data-saferedirecturl="https://www.google.com/url?hl=en&amp;q=http://www.ramanuja.org/sv/bhakti/archives/jun95/0023.html&amp;source=gmail&amp;ust=1478409724182000&amp;usg=AFQjCNEviAy8GTK2E87xXaJ-8ZCswdUnAQ">(View Link)</a></p>
  <p>  <strong>Why does Sri V Sadagopan Swami write:</strong>&nbsp;Sri V Sadagopan Swami's primary goal was and continues to be vidya danam - to educate the interested readers about the &nbsp;greatness of the illustrious sampradayam that has been bequeathed by our Elders to us and to re-kindle the efforts to serve Acharyas and encourage bhagavat-bhagavata kaimkaryams. The medium of English which is an universal language now was put to maximum use by Sri V Sadagopan Swami to enlighten those who have either relocated to countries outside of India and/or are more conversant with English than Sanskrit, Tamil, Manipravalam or other regional languages of India. &nbsp;Sri V Sadagopan Swami has used his writings to build a bridge for others to benefit from the rare granthams and great works of Acharyas, which are usually taught only for a selected few through traditional kalakshepam route. &nbsp;The many grateful emails that he receives from good people, thanking him for his kindness in &ldquo;spoon-feeding&rdquo; them on many intricate and subtle details of philosophy and worship are proof positive of the power of his writings. </p>
  <p><strong>Oppiliappan e-group:&nbsp;</strong> Sri V Sadagopan Swami started the Oppiliappan e-group in yahoogroups to continue with his likhita kaimkaryams post bhakti list. During this time Sri V Sadagopan Swami was also the conceptual head and content developer for the website&nbsp;oppiliappan.org alongwith Sri Sreyas Sarangan, a graduate student at that time in an University in USA. This website contains valuable information about the Divya Desam of Thiruvinnagar/Oppiliappan Koil including the Pasurams of the Azhvars and audios on &nbsp;Suprabhatham, Prapatti, and Mangalam as well as a &nbsp;special Thirumanjana Kattiyam for Sri Oppiliappan. </p>
  <p><strong>Sadagopan.org:</strong> In 2001 under guidance from Sri V Sadagopan Swami, Dr. Anil Thakur of Australia created the portal of sadagopan.org to house the various articles that Swami posted in various e-groups, under one place for easy reference. &nbsp;As the volume of articles began to grow it was decided that a better way of presenting these articles for the next generation was required. Thus began the rudiments of e-Books creation efforts.
  </p>
  <p><strong>SUNDARASIMHAM SERIES:&nbsp;</strong>&nbsp;In 2006 as the celebration for HH the 45th pattam Srimad Azhagiyasingar's Satabhisheka Mahotsavam was being planned, a team of volunteers from different continents took up the blessed work under the benevolent guidance of Sri V Sadagopan Swami to create 108 e-Books from the articles so far posted by Sri V Sadagopan Swami in various e-groups and make that as a samarpanam to HH Srimad Azhagiyasingar. &nbsp; With the blessings of the compassionate Acharyan and a Sri Mukham from Him, the e-Books kaimkaryam was started, Here is the link to the announcement about the special website dedicated to the celebration of the Satabhisheka Mahotsava Samarpanam.&nbsp;&nbsp;<a href="http://www.ibiblio.org/sripedia/oppiliappan/archives/mar06/msg00014.html" target="_blank">(View Link)</a></p>
  <p>The series was concluded in Nov 2006 and the 108 e-Books samarpanam was made to Acharyan at His Satabhisheka utsavam at Srirangam &nbsp; Sri Stuti was the first book to be released in this series and the Vaibhavam of Thiruvinnagrappan was the last e-book. </p>
  <p><strong>AHOBILAVALLI SERIES:</strong>&nbsp; Continuing the trend of releasing the e-Books as they were by now very popular and sought after by readers, the next set of 108 e-Books samarpanams were taken up under Ahobilavalli series. &nbsp; Since the first series was named after Sri Malolan and Acharyan (Sundarasimham=Azhagiyasingar), it was considered appropriate to begin the new series under Thayar's name. &nbsp;The first e-Book in this series was released on Dec 2006. Link for the announcement is at the URL:&nbsp;&nbsp;<a href="http://www.ibiblio.org/sripedia/oppiliappan/archives/dec06/msg00194.html" target="_blank">(View Link)</a></p>
  <p><strong>SRIHAYAGRIVAN SERIES:&nbsp;</strong> Swami Desika prabhandams and stotrams were already covered in Sundarasimham.org. But the need to complete the coverage of all works of Swami Desikan as well as more esoteric granthas of Purvacharyas was realized by the start of the new series under&nbsp; Srihayagrivan.org to house another 108 e-Books and more. &nbsp;This site was begun on April 2008 and completed in July 2011.&nbsp;&nbsp;<a href="https://groups.yahoo.com/neo/groups/saranagathi/conversations/messages/3700" target="_blank">
    (View Link-1)</a>;&nbsp;&nbsp;
    <a href="https://groups.yahoo.com/neo/groups/saranagathi/conversations/messages/5083" target="_blank">(View Link-2)</a>;</p>
  <p><strong>ALAMELUMANGA SERIES:&nbsp;</strong>Midway thru Srihayagrivan series, a special website to house the sthabakams of the Divine Sri LakshmI Sahasram on Sri Padmavati Thayar of Thiruchanur, composed by Sri Venkatadvari kavi was initiated.
    <a href="https://www.indiadivine.org/content/topic/1536470-sri-lakshmi-sahasram-by-sri-arasanippalai-venkatadhvari-kavi/" target="_blank">(View Link)</a></p>
  <p>The site was blessed with Srimukhams from HH Srimad Azhagiyasingar, HH Srimad Paravakottai Andavan, HH Sri Rangapriya Swami and HH Srimad Parakala Matam Jiyar. &nbsp; This website came to a nirvigna purthi during the Kartika Month Brahmotsavam of Thiruchanur Thayar on Nov 21, 2010&nbsp;&nbsp;<a href="https://groups.yahoo.com/neo/groups/saranagathi/conversations/messages/4917" target="_blank">(View Link)</a></p>
  <p><strong>GODHA SERIES OF EBOOKS:&nbsp;</strong>There are now over 70 e-Books that have been released under the latest e-Books series called as Godha Series. &nbsp; </p>
  <p><strong>Kaimkaryams</strong>: More often than not Sri V Sadagopan Swami&rsquo;s articles have been the primary source of many fund raising efforts that fructified into fulfilled kaimkaryams at many Divya Desams as follows </p>
  <p><em><u>a) Thiruvendipuram</u></em> <br>
    &nbsp;- Ratnangi kaimkaryam for Lord Devanathan, Mandapa kaimkaryam, Swarna Simhasana kaimkaryam for Swami Desikan as well as Navamani Malai thiruvabharanam for Lord Devanathan, Three strand pearl necklace (mummani kovai) thiruvabharanam for Sri Hemabjavalli Thayar. More recently Garuda Pathakkam for Swamy Desikan, a Sesha Pitam and Ratnangi for Sri Yoga Hayagrivan, Muthtangi for the Oushada Giri Sri LakshmiHayagrivan. The last three samarpanangal mentioned above have been blessed by HH Prakrutam SrImad Azhagiyasingar.</p>
  <p><em><u>b) Thuppul</u></em>: Veda Patasala kaimkaryam, Swami Desikan thirumaligai kaimkaryam, Makara &nbsp;Kandi, Prabandhasara haram, Dasavatara haram, Kati Sutra thiruvabharanangal samarpana kaimkaryam for Swami Desikan &nbsp; </p>
  <p><em><u>c) Srirangam</u></em>: the repair and renovation of Silver Clad Sri Kulasekhara padi and Vasantha Mandapa kaimkaryams at Srirangam Sri Ranganatha Swami temple </p>
  <p><em><u>d) Thiruvinnagaram</u></em>: Shenpaka Malai &nbsp;kaimkaryam, the Ruby Crown for Sri Bhumi Devi Thayar, Mutthangi for Moolavar Sri Oppiliappan and Ratnangi for utsavar Sri Ponnappan. </p>
  <p><em><u>e) SrI Villiputtur</u></em>: Mutthangi for Sri Andal and Sri Andal kondai kaimkaryam </p>
  <p><em><u>f) Sri Sannidhi Kaimkaryangal:&nbsp;</u></em>&nbsp;the yearly Chathurmasya kaimkaryam for Sri Ahobila Matam Acharyas thru coverage of 60 plus days of Aradhanam at Sri Sannidhi for the past ten+ years, the yearly Swathi thirumanjanams for Sri Malolan, the Saffron flower samarpana kaimkaryam for preparing the fragrance for Tirtha Prasadam, the Achidram, Aswameda, Kataka parayana kaimkaryam at Sri Sannidhi on Ekadasi/Dwadasi days, Sri Ashtalakshmi Swarna Haram kaimkaryam for Sri Malolan, Sri Ashtalakshmi Silver Kudam Samarpanam for Sri Malolan and Swarna Simhasana Samarpanam for HH Srimad Azhagiyasingar (HH the 45th Pattam), The Swarna Sri Lakshmi coin Haram with Mango Design for Sri Malolan, the Swarna Karnapatram for Sri Prahlada Varadar at Sri Ahobilam, Silver Kavacham for Sri Bhargava Narasimha and Sri Yogananda Narasimhar at Sri Ahobilam, the Silver Cladding of the Divine Sandals (Divya Padukais) of all Srimad Azhagiyasingars from Sri Adhi VanSatakopa Jiyar to HH the 45th Pattam Sri Narayana Yatindra Maha Desikan. The Thirumanjanam for these Padukais occur every Dwadasi day. The digitalization of the entire issues of Sri Nrusimha Priya magazine from 1945-2005 was completed and the content is used by scholars. Presenting the prasadams and tagadis from 106 Divya Desams to HH Srimad 46th Pattam Azhagiyasingar on sixtieth tirunakshatra mahotsavam (Sashtiabdha purthi). DVDs on Pancha Suktams- with meanings, Chathurveda Sandhyavandhanams with meanings for the Mantrams, Translations of Ahnika Grantham etc., </p>
  <p><em><u>g) New York:&nbsp;</u></em>&nbsp;Samprokshana kaimkaryam at SrI Ranganatha Temple at Pomona, Newyork, USA. </p>
  <p><strong>AWARDS FOR SRI V SADAGOPAN SWAMI:&nbsp; </strong></p>
  <p>1) Award - birudhu - of &quot;Sri Nrsimha Seva Rasikar&quot; conferred on Swami by HH Srimad Azhagiyasingar (45th pattam) at Srirangam during His Satabhishekam.&nbsp;&nbsp;<a href="http://www.ibiblio.org/sripedia/oppiliappan/archives/dec06/msg00083.html" target="_blank">(View Link)</a> </p>
  <p>2) Award - birudhu of &quot;Sri Vedanta Desika Padambuja Seva Rasikar&quot; conferred on Swami by Sri Vilakkoli Perumal Thooppul Vedanta Desikan charitable trust, Kanchipuram.&nbsp;&nbsp;<a href="https://groups.yahoo.com/neo/groups/saranagathi/conversations/topics/5062" target="_blank">(View Link)</a></p>
  <p>3) Award of Kaimkarya Ratnam was conferred on Sri V Sadagopan Swami by Ramanuja Mission, USA).&nbsp;&nbsp;<a href="http://www.indiadivine.org/audarya/sri-vaishnava-forum/361863-kaimkarya-rathnam-lifetime-accomplishment-award-announcement-2006-a.html" target="_blank">(View Link)</a>
  </p>
  <p><strong>Uniqueness of this site:&nbsp;</strong>The focus is to serve as a queryable database and &nbsp;a single point reference for the diverse aspects of the doctrines and practice of Vishistadvaitham, Acharyas, and Divya Desams. 400+ Free e-Books on every aspect of sampradayam have been assembled so far. &nbsp;Free audios and many rare granthams (philosophical texts) that cannot be found anywhere else have also been assembled here. &nbsp;Colorful presentation featuring high resolution pictures of various Divya Desa Divya Dampatis, Utsavams, Sri Matam and Ashramams are housed here.</p>
  <p><strong>Future Directions:&nbsp;</strong> Swamy Desikan alone has blessed us with 130 Sri Suktis as our Kula Dhanam. They break down into following six categories: </p>
  <p>A. Sanskrit Texts dealing with &nbsp;Philosophy and Religion (25)<br>
    B. Literary Works in Sanskrit (15)<br>
    C. Stotrams &nbsp;or Devotional Lyrics (30)<br>
    D. Rahasya Grantams in Manipravalam (34)<br>
    E. Tamil Sri Suktis (24)<br>
    F. The Miscellaneous works  (20)</p>
  <p>Among these 130 works of Swamy Desikan, atleast 15 are no longer available to us (e.g) Adhikarana Darpana, Nigama Parimalam, Ammanaippa, Usarpa, Yesarpaa, Kalarppa, Pantuppa, Thirumudiyadaivu, Silpa Sastra, Vedartha Sangraha Vyakhyana, Madhurakavi Hrudayam, Steya Virodha, Samasya Sahasra, Cakara Samarthana , Rasabhaumamruta &nbsp;and Vrukshabhaumamruta are no longer available to us. &nbsp;We have covered so far the 28 stotrams, 21 Tamil Prabandhams, 5 Kavyams and 28 Rahasya grantams in Manipravalam.</p>
  <p>33 Sri Suktis need to be covered yet as eBooks in the now revamped website. &nbsp;Similar effort will be extended to cover the uncovered Sri Suktis of other Sri Vaishnava Acharyas like Kuresar, Parasara Bhattar and others. We seek your continued patronage and blessings to further these kaimkaryams.
  </p>
  <p>May Lord Sri Hayagrivan bless us with Divine Knowledge and Strength to cover them!<br>
    May Lord Sri Hayagrivan bless all the volunteers and the users of this site with all auspiciousness! </p>
