<strong>Welcome to the revamped sadagopan.org web portal!</strong><br>
Some 20 years ago the nascent idea of this website was formed with the intention of introducing the extraordinary works of SrI Vaishnava Acharyas and Azhwars to the reading public at large, both in India and abroad, through the medium of English language in the form eBooks. That humble effort has now blossomed into a rich and frequently used website housing over 400 eBooks. It is satisfying to know that these books are being actively used by many across the globe. A dedicated team of software professionals of Sri Hayagriva Likhitha Kaimkarya Ghoshti has conceptualized the design of this new website with the singular goal of making the site user friendly â€“ with enhanced search options, seamless navigation, simple clutter-free interface and easy access across mobile as well as traditional platforms. Lovingly nurtured by devoted volunteers from different parts of the world, the team members of Sri Hayagriva Likhita Kaimkarya Ghoshti take pleasure and pride in presenting the newly revamped site for all interested readers to visit and enjoy. For those curious about the evolution of this kaimkaryam from its early days to the present, a brief glimpse of key stages in the transformation of this website over the course of many years is provided separately. To view the same click on the READ MORE link below.<br>
Though much has been accomplished through the Divine Grace of Acharyas, a lot of work that needs to be done remains yet, such as covering additional books bequeathed to us by many of our revered Acharyas.<br>
