<div id="accordion">
  <h4 class="accordion-toggle">What is Sadagopan.Org?</h4>
  <div class="accordion-content default">
    <p>Sadagopan.org is a website/portal for an exclusive library of Sri Vaishnava E-Books.  These books are based on Vishistadvaita siddhantham established by Sri Ramanuja.</p>
  </div>
  <h4 class="accordion-toggle">How can I search the E-Books?</h4>
  <div class="accordion-content">
  <p>The E-Books under this portal have been categorized and organized to make it more user-friendly in order to search the content easily.  The search pages can be accessed either using the hyperlinks in the menu bar on the left or using the hyperlinks on the search pages itself. (screenshots below)<br>
    <img src="images/searchImg.png" width="727" height="233" alt=""/>    <br>
  </p>
  </div>
  <h4 class="accordion-toggle">What type of Searches are available?</h4>
  <div class="accordion-content">
      <p>
        Following types of searches are available under this portal:
        <br>
        <strong>1.	Quick Search:</strong> This page helps you to search specific categories of E-Books to browse quickly.  The following options are available under this page.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a. Ashta Prabandham  <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b. Desika Prabandham<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c. Divya Prabahdham<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;d. Stuti, Stotram, Stavam<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;e. Gadyam, Satakam, Suktam<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;f. Chillarai Rahasyam<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;g. Kritis and<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;h. Miscellaneous<br>
        You can select one of the checkboxes (screenshot below) and click on ‘Search’ to get the results.<br>
        <img src="images/catSearchImg.png" width="712" height="145" alt=""/> 
      </p>
      <p><strong>2.	List by Series:</strong> The E-Books have been released under different ‘Series’ names.  They are:
        <br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a. Sundarasimham<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b. Ahobilavalli<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c. Sri Hayagrivan<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;d. Alamelumanga<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;e. Godha<br><br>
	    In this search page you can select a specific series by clicking on the checkbox (screenshot below) and click on ‘Search’.  The search result will list the E-Books released under that series.<br>
		<img src="images/listSearchImg.png" width="715" height="62" alt=""/> 
		<p><strong>3.	Alphabetical Search:</strong> On this page you can select an alphabet to search all E-Books starting with that alphabet (screenshot below).  No link will be available if there are no books available under that alphabet.<br>
		  <img src="images/alphaSearchImg.png" width="777" height="61" alt=""/>
		<p><strong>4. List by Title:</strong> This option is provided to enable searching E-Books by either full or part of the E-Book name.  Enter the text in the box provided and click on ‘Search’.<br>
		  <img src="images/titleSearchImg.png" width="759" height="34" alt=""/> 
		<p><strong>5.	List By Category:</strong> The E-Books have been classified in multiple categories and sub-categories.  This is a more exhaustive way of searching E-Books.  To do a search first click on the ‘Category’ drop-down box and select a category.  This will populate the sub-category drop-down list from which you can select an item.  Then click on ‘Search’.<br><br>
	    Following are the major categories available on this page:<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>a. eBook Series:</strong> Under this category you’ll be able to select books under one of the Sadagopan.org series (Sundarasimham, Ahobilavalli, Sri Hayagrivan, Alamelumanga or Godha).<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>b. Composed by Azhvars:</strong> This category helps list the titles that are composed by Azhvars.   You can select the name of an Azhvar from the sub-category list.<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>c. Composed by Acharyas:</strong> This category helps list the titles that are composed by Acharyas.   You can select the name of an Acharya from the sub-category list.<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>d. Divya Desams:</strong> This category helps find E-Books relating to specific Divya Desams.  After selecting this category, pick the Divya Desam from the sub-category list to do the search.<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>e. Deity:</strong> You can choose this category to search for E-Books on specific Deity, for example if you would like to list the E-Books on Lord Ranganatha, select ‘Deity’ from the category list and then select ‘Ranganathar’ from the sub-category list and then click on ‘Search’.<br>  
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>f. Sampradaya Texts:</strong> Select this category to choose E-Books for specific titles relating to topics on Agama, Anushtana, Chillarai Rahasya, etc.<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>g. Stotras & Prabandhams:</strong> This category will help you find E-Books on Stutis, Prabandhams, Ashtotharam, etc., based on your selection in the sub-category.<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>h. Commentary By:</strong> Select this to choose E-Books by a specific ‘commentator’ listed under sub-category.
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>i. Language:</strong> Select this category to choose Sanskrit, Tamil, Telegu or English E-Books.<br>
	    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>j. Miscellaneous:</strong>  Check this box and click on ‘Search’ to list all E-Books categorized as ‘miscellaneous’.<br>
	    Note: You may need to scroll thru the sub-categories list as all the items may not show up in the drop-down list window.<br>
	    
	    <p><strong>6. Site-wide Search or Domain Search:</strong>  This search box is provided to do a ‘string search’ in the entire portal including the search within E-Books.<br>
		  <img src="images/domSearchImg.png" width="347" height="104" alt=""/> 
	  </p>
	  </p>
